DROP TABLE IF EXISTS adif_SALIDAS;
DROP TABLE IF EXISTS adif_LLEGADAS;

CREATE TABLE adif_SALIDAS (
    HORA TIME,
    DESTINO VARCHAR(255),
    TREN VARCHAR(100),
    VIA INT,
    PRIMARY KEY (HORA, TREN, DESTINO)
);

CREATE TABLE adif_LLEGADAS (
    HORA TIME,
    ORIGEN VARCHAR(255),
    TREN VARCHAR(100),
    VIA INT,
    PRIMARY KEY (HORA, TREN, ORIGEN)
);

